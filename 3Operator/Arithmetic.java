public class Arithmetic {
    int a = 25;
    int b = 5;
    int c;
    public void add() {
        c = a+b;
        System.out.println("a+b="+c);
    }
    public void sub() {
        c = a-b;
        System.out.println("a-b="+c);
    }
    public void mul() {
        c = a*b;
        System.out.println("a*b="+c);
    }
    public void div() {
        c = a/b;
        System.out.println("a/b="+c);
    }
    public void mod() {
        c = a%b;
        System.out.println("a+b="+c);
    }
}
